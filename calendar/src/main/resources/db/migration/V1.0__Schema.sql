DROP TABLE IF EXISTS TASKS;
DROP TABLE IF EXISTS IMPORTANT_DATES;
DROP TABLE IF EXISTS DAY_TASKS;


CREATE TABLE TASKS
(
   ID INTEGER PRIMARY KEY,
   NAME VARCHAR (255),
   TASK_TIME VARCHAR (255),
   UNIQUE
   (
      NAME,
      TASK_TIME
   )
);
CREATE TABLE IMPORTANT_DATES
(
   ID INTEGER PRIMARY KEY,
   DAY INT,
   MONTH INT,
   YEAR INT,
   UNIQUE
   (
      DAY,
      MONTH,
      YEAR
   )
);
CREATE TABLE DAY_TASKS
(
   ID INTEGER PRIMARY KEY,
   TASK_ID INT,
   DATE_ID INT,
   UNIQUE
   (
      TASK_ID,
      DATE_ID
   )
);
